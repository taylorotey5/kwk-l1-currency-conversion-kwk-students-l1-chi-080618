def calculator (user_answers, num1, num2)
  if user_answer == "add"
    add(num1,num2)
    elsif user_answer == "subtract"
    subtract (num1, num2)
    elsif user_answers == "multiply"
    multiply(num1, num2)
    elsif user_answers == "divide"
    divide(num1, num2)
  end
end

def add(num1, num2)
  return num1 + num2
end

def subtract(num1, num2)
  return num1 - num2
end

def multiply(num1, num2)
  return num1 * num2
end

def divide(num1,num2)
  return num1/num2
end












